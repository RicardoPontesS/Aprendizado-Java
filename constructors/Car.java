public class Car {
	
	String brand;
	String model;
	int passengersNumber;
	double fuelCapacity;
	double fuelConsumption;

	public Car() {
		
	}
	
	public Car(String brand, String model, int passengersNumber, double fuelCapacity, double fuelConsumption) {
		super();
		this.brand = brand;
		this.model = model;
		this.passengersNumber = passengersNumber;
		this.fuelCapacity = fuelCapacity;
		this.fuelConsumption = fuelConsumption;
	}
	
}
