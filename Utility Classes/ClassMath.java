package videoaulas;

public class ClassMath {

	public static void main(String[] args) {
		
		System.out.println("Potência: " + Math.pow(2, 2));
		System.out.println("Arredondamento: " + Math.round(4.5));
		System.out.println("Arredondamento para cima: "+Math.ceil(4.3));
		System.out.println("Arredondamento para baixo: "+Math.floor(4.9));
		System.out.println("Seno: "+Math.sin(Math.toRadians(30)));
		System.out.println("Cos: "+Math.cos(Math.toRadians(1)));
		System.out.println("Tangente: "+Math.tan(Math.toRadians(45)));
	}

}
